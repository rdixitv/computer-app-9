/* Program 4
Write a program to assign values to byte, short, int, float, double, long, boolean, char & string. Print out these values with a valid prompt.
05/06/23 */

public class Program4 {
  public static void main() {
    byte b = 1;
    short s = 2;
    int n = 3;
    long l = 4;
    float f = 5.3f;
    double d = 5.2;
    boolean bl = true;
    char c = 'C';
    String str = "string";
    System.out.println("Byte value: " + b + "\nShort value: " + s);
    System.out.println("Int value: " + n + "\nLong value: " + l);
    System.out.println("Float value: " + f + "\nDouble value: " + d);
    System.out.println("Boolean value: " + bl);
    System.out.println("Character value: " + c);
    System.out.println("String value: " + str);
  }
}
